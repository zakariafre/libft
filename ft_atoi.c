#include "libft.h"

int	ft_atoi(const char	*str)
{
	int			i;
	long long	res;
	int			sign;

	i = 0;
	res = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] && ft_isdigit(str[i]))
	{
		res = res * 10 + (str[i] - 48);
		if (res > 9223372036854775807LL && sign == 1)
			return (0);
		if (res > 9223372036854775807LL && sign == -1)
			return (-1);
		i++;
	}
	return ((int)(res * sign));
}
